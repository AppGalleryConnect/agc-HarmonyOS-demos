package com.smarket.hdc2023.bean;

public class Partner {
    public String title;
    public String coverImageRealPath;
    public int order;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCoverImageRealPath() {
        return coverImageRealPath;
    }

    public void setCoverImageRealPath(String coverImageRealPath) {
        this.coverImageRealPath = coverImageRealPath;
    }

    public int getOrder() {
        return order;
    }

    public void setOrder(int order) {
        this.order = order;
    }
}
